/* ============= Data Type =============*/

class LearnDataType{

	public static void main(String[] args){

		int num = 1000;
		long bigNum = 10000000;
		short short_num = 12300;
		byte b_num = 123;
		float num1 = 123.4f;
		double d_num = 1234454.5;
		boolean isTrue = true;
		char ch = 'D';

		System.out.println("Integer : " + num);
		System.out.println("Long : " + bigNum);
		System.out.println("Short Num : " + short_num);
		System.out.println("Byte : " + b_num);
		System.out.println("Float : " + num1);
		System.out.println("Double : " + d_num);
		System.out.println("Boolean : " + isTrue);
		System.out.println("Character: " + ch);

	}
}
/* Output :

Integer : 1000
Long : 10000000
Short Num : 12300
Byte : 123
Float : 123.4
Double : 1234454.5
Boolean : true
Character: D
*/
